-- Practice Problems for Module 5

-- The problems use the Intercollegiate Athletics database as described in the background document. The course website also contains CREATE TABLE and INSERT statements for Oracle and PostgreSQL.

-- 1. List the event number, date held, customer number, customer name, facility number, and facility name of 2022 events placed by Boulder customers.


-- 2. List the customer number, customer name, event number, date held, facility number, facility name, and estimated audience cost per person (EstCost / EstAudience) for events held on 2022, in which the estimated cost per person is less than $0.2


-- 3. List the customer number, customer name, and total estimated costs for Approved events. The total amount of events is the sum of the estimated cost for each event. Group the results by customer number and customer name.


-- 4. For event plans with a work date in 2022, summarize the event plans managed by each employee and work date month. The result should include the employee number, employee name, month, count of event plans, and sum of the estimated cost of the associated event requests. For Oracle, you can use the EXTRACT function to extract the month of a date column. For PostgreSQL, you can use the DATE_PART function to extract the month of a date column.


-- 5. Insert yourself as a new row in the Customer table.


-- 6. Increase the rate by 10 percent of the resource with name nurse in the ResourceTbl table.


